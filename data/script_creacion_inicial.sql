		@periodo_prestacion_real,
		@fecha_pago_real_checked,
		@fecha_pago_real,
		@numero_de_factura,
		@medio_de_pago,
		@banco,
		@numero_de_cheque,
		@monto,
		@retenciones_ganancias,
		@retenciones_ingresos_brutos_caba,
		@retenciones_ingresos_brutos_provincia,
		@persona,
		@observaciones,
		@sucursal,
		@rubro
	)
END
