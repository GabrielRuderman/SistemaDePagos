CREATE PROCEDURE SIDOM.crear_usuario (
	@nombre_de_usuario varchar(40),
	@contrasena varchar(30),
	@prioridad_permiso int
)
AS
  BEGIN
	DECLARE @id_permiso int
	SELECT @id_permiso = id_permiso FROM SIDOM.permisos WHERE prioridad = @prioridad_permiso
	INSERT INTO SIDOM.usuarios (nombre_de_usuario, password_hash, id_permiso)
	VALUES (@nombre_de_usuario, HASHBYTES('MD5', @contrasena), @id_permiso)
  END
GO

CREATE PROCEDURE SIDOM.eliminar_usuario (@id_usuario int)
AS
	DELETE FROM SIDOM.usuarios WHERE id_usuario = @id_usuario
GO

CREATE FUNCTION SIDOM.verificar_login (
	@nombre_de_usuario varchar(40),
	@contrasena varchar(30)
) RETURNS bit
AS
  BEGIN
	DECLARE @retorno int
	IF EXISTS(  SELECT * FROM SIDOM.usuarios
				WHERE nombre_de_usuario = @nombre_de_usuario AND password_hash = HASHBYTES('MD5', @contrasena))
		SELECT @retorno = P.prioridad FROM SIDOM.usuarios U
		JOIN SIDOM.permisos P ON U.id_permiso = P.id_permiso
		WHERE U.nombre_de_usuario = @nombre_de_usuario AND U.password_hash = HASHBYTES('MD5', @contrasena)
	ELSE
		SET @retorno = 0
	RETURN @retorno
  END
GO

