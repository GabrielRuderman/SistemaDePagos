-- Crear pago --
CREATE PROCEDURE SIDOM.crear_pago
(
	@periodo_prestacion_real datetime,
	@fecha_pago_real_checked bit,
	@fecha_pago_real datetime,
	@numero_de_factura varchar(8),
	@medio_de_pago varchar(80),
	@banco varchar(80),
	@numero_de_cheque int,
	@monto money,
	@retenciones_ganancias money,
	@retenciones_ingresos_brutos_caba money,
	@retenciones_ingresos_brutos_provincia money,
	@persona varchar(100),
	@observaciones varchar(500),
	@sucursal varchar(80),
	@rubro varchar(80)
)
AS
BEGIN
	INSERT INTO SIDOM.pagos (
		periodo_prestacion_real,
		fecha_pago_real_checked,
		fecha_pago_real,
		numero_de_factura,
		medio_de_pago,
		banco,
		numero_de_cheque,
		monto,
		retenciones_ganancias,
		retenciones_ingresos_brutos_caba,
		retenciones_ingresos_brutos_provincia,
		persona,
		observaciones,
		sucursal,
		rubro
