-- Modificar pago --
CREATE PROCEDURE SIDOM.modificar_pago
(
	@id_pago int,
	@periodo_prestacion_real datetime,
	@fecha_pago_real_checked bit,
	@fecha_pago_real datetime,
	@numero_de_factura varchar(8),
	@medio_de_pago varchar(80),
	@banco varchar(80),
	@numero_de_cheque int,
	@monto money,
	@retenciones_ganancias money,
	@retenciones_ingresos_brutos_caba money,
	@retenciones_ingresos_brutos_provincia money,
	@persona varchar(100),
	@observaciones varchar(500),
	@sucursal varchar(80),
	@rubro varchar(80)
)
AS
BEGIN
	UPDATE SIDOM.pagos SET
		periodo_prestacion_real = @periodo_prestacion_real,
		fecha_pago_real_checked = @fecha_pago_real_checked,
		fecha_pago_real = @fecha_pago_real,
		numero_de_factura = @numero_de_factura,
		medio_de_pago = @medio_de_pago,
		banco = @banco,
		numero_de_cheque = @numero_de_cheque,
		monto = @monto,
		retenciones_ganancias = @retenciones_ganancias,
		retenciones_ingresos_brutos_caba = @retenciones_ingresos_brutos_caba,
		retenciones_ingresos_brutos_provincia = @retenciones_ingresos_brutos_provincia,
		persona = @persona,
		observaciones = @observaciones,
		sucursal = @sucursal,
		rubro = @rubro
	WHERE id_pago = @id_pago
